SECAO DATA
NEG2: CONST -2
DOIS: CONST 2
NUM: SPACE
NUM2: SPACE 4

SECAO TEXT
load DOIS
input NUM
ADD NEG2
MUL DOIS
STORE NUM
DIV DOIS
STORE NUM2+2
COPY NUM, NUM2+1
OUTPUT NUM2+1
OUTPUT NUM2+2
JMP NEGATIVE

POSITIVE:
LOAD NEG2
JMPP ZERO
SUB DOIS

NEGATIVE: 
LOAD DOIS
JMPN POSITIVE
SUB DOIS

ZERO:
LOAD NEG2
JMPZ FINALIZAR
SUB DOIS

FINALIZAR: STOP
